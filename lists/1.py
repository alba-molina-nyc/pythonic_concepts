# accessing elements in list

shopping_list = ['Milk', 'Cheese', 'Butter']

# list_name[index]
print(shopping_list[0])

print('Cheese' in shopping_list)

print('Peas' in shopping_list)

print(shopping_list[-1]) # negative indexing


for i in shopping_list:
    print(f'{i}, for in in shopping_list loop')

# len return value of items in the container
# range works n - 1 so it works together with len to get the right number
for i in range(len(shopping_list)):
    print(f'{i}, for i in RANGE(LEN')
    shopping_list[i] = shopping_list[i]
